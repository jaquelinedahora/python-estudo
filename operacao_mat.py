# Escreva um programa que pede que o usuário de entrada em dois valores, em seguida
# exiba em tela o resulta da soma, subtracao, multiplicacao e divisao desses numeros


num1 = int(input('Digite o primeiro número: '))
num2 = int(input('Digite o segundo número: '))

print(f'A soma de {num1} com {num2} é: {num1 + num2}')
print(f'A divisão de {num1} com {num2} é: {num1 / num2}')
print(f'A multiplicação de {num1} com {num2} é: {num1 * num2}')